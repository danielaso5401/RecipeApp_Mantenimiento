import 'package:flutter/cupertino.dart';

class RecipeFav {
  int id;
  String title;
  String url;
  String image;
  String label;
  String source;
  RecipeFav(id, title, url, image, label, source) {
    this.id = id;
    this.title = title;
    this.url = url;
    this.image = image;
    this.label = label;
    this.source = this.source;
  }
}
